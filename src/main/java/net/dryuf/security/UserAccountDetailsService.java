package net.dryuf.security;

import net.dryuf.security.bo.UserAccountBo;
import org.apache.logging.log4j.LogManager;
import org.apache.logging.log4j.Logger;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;

import javax.inject.Inject;


public class UserAccountDetailsService extends java.lang.Object implements UserDetailsService
{
	protected Logger		logger = LogManager.getLogger(getClass());

	@Inject
	protected UserAccountBo		userAccountBo;

	public UserDetails		loadUserByUsername(String username)
	{
		UserAccount user = userAccountBo.loadByUsername(username);
		logger.debug("checking user "+username+": "+user);

		if (user == null) {
			throw new UsernameNotFoundException("User not found for OpenID: "+username);
		}
		else {
			/*if (false) {
				throw new DisabledException("User is disabled");
			}*/
			return new UserAccountDetails(user, userAccountBo);
		}
	}
}
